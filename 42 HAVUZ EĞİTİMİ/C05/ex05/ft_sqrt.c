int	ft_sqrt(int nb)
{
	long	root;
	long	b;

	b = nb;
	root = 1;
	if (b >= 1)
	{
		while (root * root <= b)
		{
			if (root * root == b)
			{
				return (root);
			}
			root++;
		}
	}
	return (0);
}

#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	b;
	int	i;

	b = argc - 1;
	while (argv[b] != NULL && b != 0 && argc)
	{
		i = 0;
		while (argv[b][i] != '\0')
		{
			write(1, &argv[b][i], 1);
			i++;
		}
		write(1, "\n", 1);
		b--;
	}
	return (0);
}
